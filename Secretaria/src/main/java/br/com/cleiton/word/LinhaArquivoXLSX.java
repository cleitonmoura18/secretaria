package br.com.cleiton.word;

public class LinhaArquivoXLSX {
	private Long idEncontrista;
	private String nome;
	private String nomeConjugue;
	private String equipe;
	private String papel;
	private String tipo;

	public Long getIdEncontrista() {
		return idEncontrista;
	}

	public void setIdEncontrista(Long idEncontrista) {
		this.idEncontrista = idEncontrista;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getNomeConjugue() {
		return nomeConjugue;
	}

	public void setNomeConjugue(String nomeConjugue) {
		this.nomeConjugue = nomeConjugue;
	}

	public String getEquipe() {
		return equipe;
	}

	public void setEquipe(String equipe) {
		this.equipe = equipe;
	}

	public String getPapel() {
		return papel;
	}

	public void setPapel(String papel) {
		this.papel = papel;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

}
